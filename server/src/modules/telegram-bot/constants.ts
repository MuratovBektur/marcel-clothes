// Из выбранных возрастных категорий собирает: сами категории (поле "пол", хранится как jsonb-массив)
// и объединённый список размеров (без дублей, размеры по категории — см. AGE_CATEGORY_SIZES).
export function buildAgeCategoryFields(selected: string[]): {
  gender: string[];
  sizes: string[];
} {
  return {
    gender: [...selected],
    sizes: [...new Set(selected.flatMap((category) => AGE_CATEGORY_SIZES[category] ?? []))],
  };
}

